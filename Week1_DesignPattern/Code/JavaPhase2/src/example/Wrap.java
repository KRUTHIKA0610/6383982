package example;

public class Wrap {
public static void main(String [] args) {
	Integer i=Integer.valueOf(78);
	System.out.println(i);
	
	
	Long a=Long.valueOf(1234567899);
	System.out.println(a);
	
	
	Double b=Double.valueOf(123456789.00000866899);
	System.out.println(b);
	
	
	Float c=Float.valueOf(45);
	System.out.println(c);
	
	
	Boolean d=Boolean.valueOf(false);
	System.out.println(d);
	
	
	Character e=Character.valueOf('0');
	System.out.println(e);
	
	
	Byte f=Byte.valueOf((byte)8);
	System.out.println(f);
	
	
	Short g=Short.valueOf((short)4);
	System.out.println(g);	
	
	
	
}
}
